//Mostrar las tablas de multiplicar del 1 al 9.
public class PrAp0318 {

	public static void main(String[] args) {
		for (int i = 1; i < 10; i++) {
			System.out.println();
			System.out.println("TABLA DE MULTIPLICAR DEL "+i);
			for (int j = 0; j <=10; j++) {
				System.out.println(i +" x "+j+" = "+i*j);
				
			}
			
		}
		

	}

}
